def add (a,b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    if b == 0:
        return "ERROR: Cannot divide by zero"
    return a/b

def power(a,b):
    return a**b

def squareroot(a):
    return a**0.5

def cube(a):
    return a**3

def mod(a,b):
    return a%b